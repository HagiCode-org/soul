import fs from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

import { orthogonalCatalogOverrides } from './reference-material-overrides.mjs'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const monoRoot = path.resolve(repoRoot, '..', '..')

const mainDocPath = path.join(monoRoot, 'repos', 'web', 'docs', '50组SOUL.md核心Catalog（风格差异化+高辨识度+强适配性）.md')
const orthDocPath = path.join(monoRoot, 'repos', 'web', 'docs', '10组正交维度Catalog2（可与50组主Catalog交叉生成500个独家人设）.md')
const outputPath = path.join(repoRoot, 'src', 'data', 'reference-materials.generated.ts')

function cleanCell(value) {
  return value
    .replace(/<br\s*\/?>/gi, '\n')
    .replace(/^\s+|\s+$/g, '')
}

function parseMarkdownTable(markdown) {
  return markdown
    .split('\n')
    .map((line) => line.trim())
    .filter((line) => line.startsWith('|'))
    .filter((line) => !/^\|(?:\s*-+\s*\|)+$/.test(line))
    .map((line) => line.slice(1, -1).split('|').map(cleanCell))
}

function parseMainCatalogs(markdown) {
  const lines = markdown.split('\n')
  let currentCategory = ''
  const entries = []

  for (const line of lines) {
    const headingMatch = line.match(/^###\s+[一二三四五六七八九十]+、(.+?)(?:（.*）)?$/)
    if (headingMatch) {
      currentCategory = headingMatch[1].trim()
      continue
    }

    if (!line.trim().startsWith('|')) {
      continue
    }

    const cells = line.trim().slice(1, -1).split('|').map(cleanCell)
    if (cells.length !== 4 || cells[0] === '编号' || cells[0] === '---') {
      continue
    }

    const index = Number.parseInt(cells[0], 10)
    if (!Number.isFinite(index)) {
      continue
    }

    entries.push({
      index,
      category: currentCategory,
      name: cells[1],
      core: cells[2],
      signature: cells[3],
    })
  }

  return entries
}

function parseOrthogonalCatalogs(markdown) {
  const tableRows = parseMarkdownTable(markdown)
  const entries = []

  for (const cells of tableRows) {
    if (cells.length !== 5 || cells[0] === '编号' || cells[0] === '---') {
      continue
    }

    const index = Number.parseInt(cells[0], 10)
    if (!Number.isFinite(index)) {
      continue
    }

    entries.push({
      index,
      name: cells[1],
      core: cells[2],
      signature: cells[3],
      compatibility: cells[4],
    })
  }

  return entries
}

function mergeOrthogonalCatalogs(baseEntries, overrideEntries) {
  const merged = [...baseEntries]
  const usedIndexes = new Set(baseEntries.map((entry) => entry.index))

  for (const entry of overrideEntries) {
    if (usedIndexes.has(entry.index)) {
      throw new Error(`Duplicate orthogonal catalog index: ${entry.index}`)
    }

    merged.push(entry)
    usedIndexes.add(entry.index)
  }

  return merged.sort((left, right) => left.index - right.index)
}

async function main() {
  const [mainMarkdown, orthMarkdown] = await Promise.all([
    fs.readFile(mainDocPath, 'utf8'),
    fs.readFile(orthDocPath, 'utf8'),
  ])

  const mainCatalogs = parseMainCatalogs(mainMarkdown)
  const orthogonalCatalogs = parseOrthogonalCatalogs(orthMarkdown)

  if (mainCatalogs.length !== 50) {
    throw new Error(`Expected 50 main catalogs, received ${mainCatalogs.length}.`)
  }

  if (orthogonalCatalogs.length < 10) {
    throw new Error(`Expected at least 10 orthogonal catalogs from docs, received ${orthogonalCatalogs.length}.`)
  }

  const mergedOrthogonalCatalogs = mergeOrthogonalCatalogs(orthogonalCatalogs, orthogonalCatalogOverrides)

  const generated = {
    generatedAtUtc: new Date().toISOString(),
    provenance: {
      mainPath: 'repos/web/docs/50组SOUL.md核心Catalog（风格差异化+高辨识度+强适配性）.md',
      orthPath: 'repos/web/docs/10组正交维度Catalog2（可与50组主Catalog交叉生成500个独家人设）.md',
      combinationFormula: '50 x 10 = 500',
    },
    mainCatalogs,
    orthogonalCatalogs: mergedOrthogonalCatalogs,
  }

  const fileContent = [
    '// This file is generated by scripts/generate-reference-materials.mjs',
    `export const referenceCatalogSnapshot = ${JSON.stringify(generated, null, 2)} as const`,
    '',
  ].join('\n')

  await fs.writeFile(outputPath, fileContent, 'utf8')
  process.stdout.write(`Generated ${outputPath}\n`)
}

main().catch((error) => {
  console.error(error)
  process.exitCode = 1
})
